#pragma once

class Living
{
protected:
	double maxHealthPoints;
	double HealthPoints;
	double AttackDamage;
	double Shield;
	double Stamina;
	double maxStamina;

	//mutator
	bool SetHealth(double value);

	//mutator
	void SetAttack(double value);

	Living() 
	{
		maxHealthPoints = 100;
		HealthPoints = 100;
		AttackDamage = 10;
		Shield = 0;
		Stamina = 100;
		maxStamina = 100;
	}
public:
	////////////////////////////////////////////////////////////////////////////////////////
	//Default constructor			
	//
	//\brief   sets the object's values such as health and etc...
	//
	//\param hp   Health		
	//\param ad   Attack
	////////////////////////////////////////////////////////////////////////////////////////

	Living(double hp, double ad);

	////////////////////////////////////////////////////////////////////////////////////////
	//Health modifier		
	//
	//\brief   increase health
	//
	//\param value   a value to increase health by												
	////////////////////////////////////////////////////////////////////////////////////////

	void IncHealth(double value);

	////////////////////////////////////////////////////////////////////////////////////////
	//Health modifier				
	//
	//\brief   decrease health
	//
	//\param value   a value to decrease health by															
	////////////////////////////////////////////////////////////////////////////////////////

	void DecHealth(double value);


	////////////////////////////////////////////////////////////////////////////////////////
	//Health modifier		
	//
	//\brief   increase health
	//
	//\param value   a value to increase health by												
	////////////////////////////////////////////////////////////////////////////////////////

	void IncStamina(double value);

	////////////////////////////////////////////////////////////////////////////////////////
	//Health modifier				
	//
	//\brief   decrease health
	//
	//\param value   a value to decrease health by															
	////////////////////////////////////////////////////////////////////////////////////////

	void DecStamina(double value);

	////////////////////////////////////////////////////////////////////////////////////////
	//Default constructor			
	//
	//\brief   sets the details of leveling
	//
	//\param 													
	////////////////////////////////////////////////////////////////////////////////////////

	void setLevelUps();

	////////////////////////////////////////////////////////////////////////////////////////
	//Default constructor			
	//
	//\brief   Levels living object up based on 'setLevelUps' function values
	//													
	////////////////////////////////////////////////////////////////////////////////////////

	void LevelUp();

	////////////////////////////////////////////////////////////////////////////////////////
	//Tester			
	//
	//\brief   Tests if the object is alive													
	////////////////////////////////////////////////////////////////////////////////////////

	bool isLiving();

	double getAttack();

	double reStamina();

	~Living();
};